							<h3>A.B. House</h3>
							<h4>Andreescu & Gaivoronschi Architects</h4>
							<p>
								The house is built in a residential area in the north of Timisoara, Romania. Nearby, there is another Andreescu & Gaivoronschi house designed 13 years ago, which developed the same idea of a house on a house. It has to do with social phenomena typical for Italy and Greece, where young members of the family live with the parents. This coexistence was sincerely expressed in both cases: the young house on the old one. Wood means youth and freshness, whereas stone has to do with the roots, the passing of time. </p>
								<p>The result is a wooden house on a bigger stone house. More than that, this juxtaposition has to do with a public/private dialectic. Stone, solid walls defend and close a little the private area. In A.B. house, this character of fence house is equilibrated by the subtraction of a typical traditional grey space of public/private interaction at the entrance. Behind the house, the theme of intermediary it is represented by the patio and the covered terrace, the outside eating room. Inside the house there is a two direction topology: the horizontal one, of the parents' area, which embraces also the patio; and the vertical one which juxtaposes the spaces of the two houses. </p>
								<p>This juxtaposition also occurs in the living room area, where a typical raum-plan pattern is developed. The living room is developed as a theatre which can be observed from different levels. There is also a private part, on the first floor, similar to the women's room in traditional oriental dwellings - a space from where you can observe but not be observed. It is a space for a future library, a space for smoking. The apartment contained in the wooden house from above is enough for a young couple. The inside/outside relation is also developed here, without dismantling the unity of the box: an intermediary space to the south behind wooden louvres, a thin balcony to the west, to observe the atrium and the dawn and a vertical incision to the east, to mirror the rising sun.</p>
